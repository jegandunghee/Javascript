

// 변수 사용해보기 

// 변수 선언
let userName; 

// 출력해보기 
console.log(userName);

// 변수에 문자열 지정하기 (대입연산자)
userName = "홍길동";

// 문자열 지정 후 userName 출력해보기 
console.log(userName);

// 변수에 숫자 지정하기 
let age = 55;

console.log(age);

// let으로 선언한 변수는 변수값 수정이 가능함 
age = '춘향이';

console.log(age);

//const 사용해서 상수 지정하기 
const site = "mbc";
console.log(site);
// const 상수 값 변경하기 
site = 5;
console.log(site);